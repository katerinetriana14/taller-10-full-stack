<?php
// Definimos una constante
define('CONSTANTE', 5);

// Definimos una variable
$variable = 10;

// Sumamos la constante y la variable
$suma1 = CONSTANTE + $variable;
echo "Suma 1: " . $suma1 . "\n";

// Cambiamos el valor de la variable
$variable = 7;

// Sumamos la constante y la variable nuevamente
$suma2 = CONSTANTE + $variable;
echo "Suma 2: " . $suma2 . "\n";
?>
